package org.aifb.xxplore.shared.exception;

public class ExploreRuntimeException extends RuntimeException {

	public ExploreRuntimeException() {
		super();
	}

	public ExploreRuntimeException(String arg0) {
		super(arg0);
	}

	public ExploreRuntimeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ExploreRuntimeException(Throwable arg0) {
		super(arg0);
	}

}
